# ### 1.1 O que é o Pandas?

# O Pandas é uma biblioteca de código aberto que proporciona estruturas de dados de alto nível e ferramentas 
# para análise de dados em Python. Ele se baseia no NumPy, outro pacote popular para computação numérica em 
# Python. O Pandas é especialmente útil para lidar com dados tabulares e séries temporais, tornando-se uma 
# escolha poderosa para uma variedade de tarefas de análise de dados.

# ### 1.2 Principais Recursos do Pandas

# - **DataFrames:** O DataFrame é a estrutura de dados central do Pandas. Ele representa dados tabulares 
# com linhas e colunas, semelhante a uma planilha ou tabela SQL. Os DataFrames oferecem uma maneira intuitiva 
# e flexível de manipular e analisar dados.

# - **Series:** Uma Series é uma estrutura de dados unidimensional semelhante a um array que pode conter qualquer 
# tipo de dados. É uma das estruturas fundamentais do Pandas e é comumente usada para representar uma única 
# coluna de dados em um DataFrame.

# - **Manipulação de Dados Simplificada:** O Pandas oferece uma ampla gama de funcionalidades para filtrar, 
# ordenar, agrupar e combinar dados, tornando a manipulação e análise de conjuntos de dados complexos mais 
# simples e eficientes.

